"""
Cron Scheduler — install/uninstall cron jobs on Unix systems.

Generates crontab entries and can install them directly.
Also provides human-friendly "cron format" explanation.
"""

from __future__ import annotations

import logging
import shlex
import subprocess
import sys
from dataclasses import dataclass, field
from datetime import datetime
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

# Predefined schedule expressions
CRON_PRESETS: dict[str, str] = {
    "hourly": "0 * * * *",
    "daily": "0 8 * * *",
    "daily_midnight": "0 0 * * *",
    "weekly": "0 8 * * 1",
    "every_6h": "0 */6 * * *",
    "every_12h": "0 */12 * * *",
    "every_30min": "*/30 * * * *",
}


@dataclass
class CronTask:
    """Definition of a cron job entry.

    Attributes
    ----------
    name : str
        Comment label for the cron entry (appears after ``#``).
    command : str
        Full shell command to execute.
    schedule : str
        Cron schedule expression (5 fields).
        Can also use a preset name like ``daily``, ``hourly``.
    enabled : bool
        Whether the entry should be commented out (default True).
    log_file : str
        Log file for stdout/stderr (default ``logs/scheduler/cron.log``).
    """

    name: str = "football_pipeline"
    command: str = ""
    schedule: str = "daily"
    enabled: bool = True
    log_file: str = "logs/scheduler/cron.log"

    @property
    def cron_schedule(self) -> str:
        """Get the cron schedule expression."""
        return CRON_PRESETS.get(self.schedule, self.schedule)

    @property
    def cron_line(self) -> str:
        """Generate a single cron line."""
        parts = [
            self.cron_schedule,
            shlex.quote(self.command),
            ">>",
            shlex.quote(str(self.log_file)),
            "2>&1",
        ]

        if not self.enabled:
            parts.insert(0, "#")

        parts.append(f"# {self.name}")
        return " ".join(parts)


class CronScheduler:
    """Manages cron job integration.

    Parameters
    ----------
    project_root : str | Path
        Project root directory.
    venv_python : str | Path
        Path to venv Python executable.
    """

    def __init__(
        self,
        project_root: str | Path | None = None,
        venv_python: str | Path | None = None,
    ) -> None:
        self.project_root = Path(project_root or Path.cwd())
        self.venv_python = str(
            venv_python or self._find_venv_python(),
        )

    # ── Public API ─────────────────────────────────────

    def generate_crontab(
        self,
        tasks: list[CronTask] | None = None,
    ) -> str:
        """Generate a crontab file content for all pipeline tasks.

        Parameters
        ----------
        tasks : list[CronTask], optional
            Cron tasks to include. Generates defaults if not provided.

        Returns
        -------
        str
            Crontab content.
        """
        if tasks is None:
            tasks = self._default_tasks()

        lines = [
            f"# Football Prediction Pipeline — Crontab",
            f"# Generated by {__name__} on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
            f"#",
            f"# Install: crontab crontab.txt",
            f"# Verify:  crontab -l",
            f"",
            f"SHELL=/bin/bash",
            f"PATH=/usr/local/bin:/usr/bin:/bin",
            f"",
        ]

        for task in tasks:
            lines.append(task.cron_line)
            lines.append("")

        return "\n".join(lines)

    def save_crontab(
        self,
        path: str | Path = "crontab.txt",
        tasks: list[CronTask] | None = None,
    ) -> Path:
        """Generate and save a crontab file.

        Parameters
        ----------
        path : str | Path
            Output path.
        tasks : list[CronTask], optional
            Cron tasks.

        Returns
        -------
        Path
            Path to the saved file.
        """
        content = self.generate_crontab(tasks)
        output_path = Path(path)
        output_path.write_text(content, encoding="utf-8")
        logger.info("Saved crontab to: %s", output_path)
        return output_path

    def install_crontab(self, tasks: list[CronTask] | None = None) -> bool:
        """Install the cron jobs directly via ``crontab`` command.

        Parameters
        ----------
        tasks : list[CronTask], optional
            Cron tasks to install.

        Returns
        -------
        bool
            True if installed successfully.
        """
        content = self.generate_crontab(tasks)

        try:
            proc = subprocess.run(
                ["crontab"],
                input=content.encode(),
                capture_output=True,
                check=False,
            )
            if proc.returncode == 0:
                logger.info("Crontab installed successfully")
                return True

            logger.error("Failed to install crontab: %s", proc.stderr.decode())
            return False

        except FileNotFoundError:
            logger.error("crontab command not found — is this a Unix system?")
            return False

    def uninstall_crontab(self) -> bool:
        """Remove all cron jobs (clear the crontab).

        Warning: this removes ALL cron jobs, not just our pipeline ones.
        Use with caution.
        """
        try:
            proc = subprocess.run(
                ["crontab", "-r"],
                capture_output=True, check=False,
            )
            if proc.returncode == 0:
                logger.info("Crontab cleared")
                return True
            logger.error("Failed to clear crontab: %s", proc.stderr)
            return False
        except FileNotFoundError:
            return False

    # ── Default tasks ─────────────────────────────────

    def _default_tasks(self) -> list[CronTask]:
        """Create default cron tasks for the pipeline."""
        python = self.venv_python
        root = self.project_root
        log_dir = root / "logs" / "scheduler"

        return [
            CronTask(
                name="football_daily_pipeline",
                command=f"cd {root} && {python} -m src.scheduler.cli run --quiet",
                schedule="daily",
                log_file=str(log_dir / "daily_pipeline.log"),
            ),
            CronTask(
                name="football_cleanup",
                command=f"cd {root} && {python} -m src.scheduler.cli run --tasks clean_data,generate_logs,backup_database --quiet",
                schedule="weekly",
                log_file=str(log_dir / "weekly_cleanup.log"),
            ),
        ]

    # ── Internal helpers ───────────────────────────────

    def _find_venv_python(self) -> str:
        """Find the venv Python executable."""
        candidates = [
            self.project_root / ".venv" / "bin" / "python",
            self.project_root / ".venv" / "Scripts" / "python.exe",
            Path(sys.executable),
        ]
        for candidate in candidates:
            if candidate.exists():
                return str(candidate)
        return sys.executable
