"""
Windows Task Scheduler — install/uninstall scheduled tasks on Windows.

Generates .bat scripts that create scheduled tasks using ``schtasks``,
and can also install them directly via subprocess.

Supports:
- Daily, hourly, or custom recurrence
- Log file redirection
- Venv-aware Python execution
- Existing script integration (auto_commit.ps1, refresh_worldcup.py, etc.)
"""

from __future__ import annotations

import logging
import subprocess
import sys
from dataclasses import dataclass, field
from datetime import datetime
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)


@dataclass
class WindowsScheduleTask:
    """Definition of a Windows Task Scheduler task.

    Attributes
    ----------
    name : str
        Task name in Windows Task Scheduler (e.g. ``FootballPipeline``).
    script_path : str
        Path to the Python script to run.
    args : str
        Command-line arguments for the script (default ``--quiet``).
    schedule_type : str
        ``daily``, ``hourly``, ``onstart``, or ``custom`` (default ``daily``).
    schedule_time : str
        Time for daily schedule in HH:MM format (default ``08:00``).
    repeat_interval_minutes : int
        For hourly: interval in minutes (default 60).
    working_dir : str
        Working directory (defaults to project root).
    venv_python : str
        Path to venv Python executable.
    log_file : str
        Log file for task output (default ``logs/scheduler/windows_task.log``).
    description : str
        Task description for Task Scheduler.
    enabled : bool
        Whether the task should be enabled (default True).
    """

    name: str = "FootballPipeline"
    script_path: str = ""
    args: str = "--quiet"
    schedule_type: str = "daily"
    schedule_time: str = "08:00"
    repeat_interval_minutes: int = 60
    working_dir: str = "."
    venv_python: str = ""
    log_file: str = "logs/scheduler/windows_task.log"
    description: str = "Football prediction automated pipeline"
    enabled: bool = True


class WindowsScheduler:
    """Manages Windows Task Scheduler integration.

    Parameters
    ----------
    project_root : str | Path
        Project root directory. Auto-detected if not provided.
    venv_python : str | Path
        Path to venv Python. Auto-detected if not provided.
    """

    def __init__(
        self,
        project_root: str | Path | None = None,
        venv_python: str | Path | None = None,
    ) -> None:
        self.project_root = Path(project_root or self._find_project_root())
        self.venv_python = (
            str(venv_python) if venv_python else self._find_venv_python()
        )

    # ── Public API ─────────────────────────────────────

    def create_task_bat(self, task: WindowsScheduleTask | None = None) -> str:
        """Generate a .bat file that installs a scheduled task.

        Parameters
        ----------
        task : WindowsScheduleTask, optional
            Task configuration. Uses defaults if not provided.

        Returns
        -------
        str
            Content of the .bat file.
        """
        t = task or WindowsScheduleTask()

        python_path = t.venv_python or self.venv_python
        script_path = self._resolve_script(t.script_path)
        log_path = self.project_root / t.log_file

        # Ensure paths use forward slashes or escaped backslashes for cmd
        python = python_path.replace("/", "\\")
        script = str(script_path).replace("/", "\\")
        log = str(log_path).replace("/", "\\")
        workdir = str(self.project_root).replace("/", "\\")

        lines = [
            "@echo off",
            "chcp 65001 >nul",
            "",
            f"REM  {t.name} — {t.description}",
            f"REM  Generated by {__name__} on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
            f"REM",
            f"REM  Requirements:",
            f"REM    - Run as Administrator (right-click → Run as Administrator)",
            f"REM    - Python venv at: {python}",
            f"",
            f"REM  Create the scheduled task",
            f'schtasks /create /tn "{t.name}" /tr ',
            f'  "{python} {script} {t.args}"',
            f'  /sc {t.schedule_type}',
        ]

        if t.schedule_type == "daily":
            lines.append(f'  /st {t.schedule_time}')
        elif t.schedule_type == "hourly":
            lines.append(f'  /mo {t.repeat_interval_minutes}')
        elif t.schedule_type == "onstart":
            lines.append(f'  /delay 0005:00')  # 5 min delay after startup

        lines.extend([
            f'  /ru "%USERNAME%"',
            f'  /rl HIGHEST',
            f'  /f',
            f'  /it',
            f'  /np',
        ])

        if not t.enabled:
            lines.append(f'  /disable')

        # Add task description
        lines.extend([
            "",
            f"REM  Set working directory",
            f'schtasks /change /tn "{t.name}" /tr "{python} {script} {t.args}"',
            "",
            f"echo.",
            f"echo  Task '{t.name}' installed successfully.",
            f"echo  Schedule: {t.schedule_type}" + (
                f" at {t.schedule_time}" if t.schedule_type == "daily" else ""
            ),
            f"echo  Script: {script}",
            f"echo  Log: {log}",
            f"echo.",
        ])

        return "\n".join(lines)

    def save_task_bat(
        self,
        path: str | Path = "setup_football_pipeline.bat",
        task: WindowsScheduleTask | None = None,
    ) -> Path:
        """Generate and save a .bat file for the scheduled task.

        Parameters
        ----------
        path : str | Path
            Output path for the .bat file.
        task : WindowsScheduleTask, optional
            Task configuration.

        Returns
        -------
        Path
            Path to the saved .bat file.
        """
        content = self.create_task_bat(task)
        output_path = Path(path)
        output_path.write_text(content, encoding="utf-8")
        logger.info("Saved task installer: %s", output_path)
        return output_path

    def install_task(self, task: WindowsScheduleTask | None = None) -> bool:
        """Install the scheduled task directly (requires admin privileges).

        Parameters
        ----------
        task : WindowsScheduleTask, optional
            Task configuration.

        Returns
        -------
        bool
            True if the task was installed successfully.
        """
        t = task or WindowsScheduleTask()
        python_path = t.venv_python or self.venv_python
        script_path = self._resolve_script(t.script_path)

        cmd = [
            "schtasks", "/create", "/tn", t.name,
            "/tr", f'"{python_path}" "{script_path}" {t.args}',
            "/sc", t.schedule_type,
        ]

        if t.schedule_type == "daily":
            cmd.extend(["/st", t.schedule_time])
        elif t.schedule_type == "hourly":
            cmd.extend(["/mo", str(t.repeat_interval_minutes)])

        cmd.extend(["/f"])

        try:
            result = subprocess.run(cmd, capture_output=True, text=True, check=False)
            if result.returncode == 0:
                logger.info("Task '%s' installed successfully", t.name)
                return True
            logger.error(
                "Failed to install task: %s", result.stderr or result.stdout,
            )
            return False
        except FileNotFoundError:
            logger.error("schtasks not found — this command requires Windows")
            return False

    def uninstall_task(self, task_name: str = "FootballPipeline") -> bool:
        """Remove a scheduled task.

        Parameters
        ----------
        task_name : str
            Task name in Windows Task Scheduler.

        Returns
        -------
        bool
            True if successfully removed.
        """
        try:
            result = subprocess.run(
                ["schtasks", "/delete", "/tn", task_name, "/f"],
                capture_output=True, text=True, check=False,
            )
            if result.returncode == 0:
                logger.info("Task '%s' uninstalled", task_name)
                return True
            logger.warning(
                "Failed to uninstall task: %s", result.stderr or result.stdout,
            )
            return False
        except FileNotFoundError:
            logger.error("schtasks not found — this command requires Windows")
            return False

    def list_tasks(self) -> list[dict[str, str]]:
        """List all scheduled tasks matching the pipeline name pattern.

        Returns
        -------
        list[dict[str, str]]
            Task info: name, status, next run time.
        """
        try:
            result = subprocess.run(
                ["schtasks", "/query", "/fo", "CSV", "/v"],
                capture_output=True, text=True, check=False,
            )
            if result.returncode != 0:
                return []

            tasks: list[dict[str, str]] = []
            for line in result.stdout.splitlines()[1:]:
                parts = line.split(",")
                if len(parts) >= 3:
                    task_name = parts[0].strip('"')
                    if "Football" in task_name or "Pipeline" in task_name:
                        tasks.append({
                            "name": task_name,
                            "status": parts[1].strip('"') if len(parts) > 1 else "",
                            "next_run": parts[2].strip('"') if len(parts) > 2 else "",
                        })
            return tasks
        except FileNotFoundError:
            return []

    # ── Generators for existing project scripts ────────

    def generate_auto_commit_bat(self) -> str:
        """Generate a .bat file for the auto-commit script."""
        ps_script = self.project_root / "scripts" / "auto_commit.ps1"
        log_file = self.project_root / "auto_commit.log"

        return f"""\
@echo off
chcp 65001 >nul

REM  Auto-Commit Scheduler — commits and pushes changes hourly
REM  Generated by {__name__} on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

set PROJECT_DIR={str(self.project_root).replace('/', '\\')}
set PS_SCRIPT={str(ps_script).replace('/', '\\')}
set LOG_FILE={str(log_file).replace('/', '\\')}

REM  Create the scheduled task (hourly)
schtasks /create /tn "FootballAutoCommit" /tr \\
  "powershell.exe -ExecutionPolicy Bypass -File \\"%PS_SCRIPT%\\"" \\
  /sc hourly /mo 1 /ru "%USERNAME%" /rl HIGHEST /f /it

if %ERRORLEVEL% equ 0 (
    echo.
    echo  Auto-commit task installed successfully.
    echo  Runs every hour.
    echo  Log: %LOG_FILE%
    echo.
) else (
    echo  Failed to install auto-commit task.
    echo  Run this batch file as Administrator.
    echo.
)
"""

    def generate_value_bets_bat(self) -> str:
        """Generate a .bat file for the daily value bets script."""
        python = self.venv_python.replace("/", "\\")
        script = str(self.project_root / "today_value_bets_live.py").replace("/", "\\")
        log = str(self.project_root / "logs" / "value_bets.log").replace("/", "\\")

        return f"""\
@echo off
chcp 65001 >nul

REM  Value Bets Scheduler — runs daily at 7:00 AM
REM  Generated by {__name__} on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

schtasks /create /tn "FootballValueBets" /tr \\
  "{python} {script} --quiet" \\
  /sc daily /st 07:00 /ru "%USERNAME%" /rl HIGHEST /f /it

if %ERRORLEVEL% equ 0 (
    echo.
    echo  Value bets task installed successfully.
    echo  Runs daily at 7:00 AM.
    echo.
) else (
    echo  Failed to install value bets task.
    echo  Run this batch file as Administrator.
    echo.
)
"""

    # ── Internal helpers ───────────────────────────────

    @staticmethod
    def _find_project_root() -> Path:
        """Find the project root by looking for pyproject.toml or .git."""
        cwd = Path.cwd()
        for marker in ("pyproject.toml", ".git", "setup.py"):
            path = cwd / marker
            if path.exists():
                return cwd
        return cwd

    def _find_venv_python(self) -> str:
        """Find the venv Python executable."""
        # Check common locations
        candidates = [
            self.project_root / ".venv" / "Scripts" / "python.exe",
            self.project_root / ".venv" / "bin" / "python",
            Path(sys.prefix) / "python.exe",
        ]
        for candidate in candidates:
            if candidate.exists():
                return str(candidate)
        return sys.executable

    def _resolve_script(self, script_path: str) -> Path:
        """Resolve a script path relative to the project root."""
        path = Path(script_path)
        if not path.is_absolute():
            path = self.project_root / script_path
        return path
