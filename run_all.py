"""
run_all.py - One command to rule them all.

Orchestrates the entire prediction ecosystem:
  1. Collect data (if needed)
  2. Train LightGBM model
  3. Generate predictions
  4. Find value bets (with live odds)
  5. Launch dashboard

Usage:
    python run_all.py                       # Full run all
    python run_all.py --skip-collect        # Skip data download
    python run_all.py --skip-value-bets     # Skip value bets
    python run_all.py --skip-dashboard      # Don't open dashboard
    python run_all.py --predict-only        # Quick: predict only
"""

from __future__ import annotations

import argparse
import subprocess
import sys
import time
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent
PYTHON = sys.executable


def print_header(text: str) -> None:
    print()
    print("=" * 72)
    print(f"  {text}")
    print("=" * 72)


def run_script(script: str, args: list[str] | None = None) -> bool:
    """Run a Python script and return True on success."""
    cmd = [PYTHON, script] + (args or [])
    print(f"  $ python {script} {' '.join(args or [])}")
    print()
    result = subprocess.run(cmd, cwd=PROJECT_ROOT)
    if result.returncode != 0:
        print(f"  [X] {script} failed (exit code {result.returncode})")
        return False
    print(f"  [OK] {script} completed")
    return True


def check_data_exists() -> bool:
    """Check if World Cup data exists."""
    csv_path = PROJECT_ROOT / "data" / "raw" / "worldcup_all.csv"
    return csv_path.exists()


def get_value_bets_files() -> list[Path]:
    """Get the latest value bet report files for verification."""
    vdir = PROJECT_ROOT / "reports" / "value_bets"
    if not vdir.exists():
        return []
    return sorted(vdir.glob("value_bets_*.csv"))


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Run the full Football Prediction pipeline - all in one command",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--skip-collect", action="store_true",
        help="Skip data collection (use existing data)",
    )
    parser.add_argument(
        "--skip-value-bets", action="store_true",
        help="Skip value bets step",
    )
    parser.add_argument(
        "--skip-dashboard", action="store_true",
        help="Don't open the dashboard at the end",
    )
    parser.add_argument(
        "--predict-only", action="store_true",
        help="Predict only (skips training, uses existing model)",
    )
    parser.add_argument(
        "--model", default="lgb", choices=["lgb", "xgb", "lr", "rf"],
        help="Model type (default: lgb - LightGBM, best backtest performer)",
    )
    args = parser.parse_args(argv)

    t_start = time.time()

    print()
    print("=" * 72)
    print("  [FOOTBALL PREDICTION - RUN ALL]")
    print("  Pipeline: Collect > Train > Predict > Value Bets > Dashboard")
    print(f"  Model: {args.model.upper()}")
    print("=" * 72)

    steps = []
    all_ok = True

    # ── Step 1: Collect data ─────────────────────────────
    if args.predict_only:
        print("\n  [--predict-only] Skipping data collection")
    elif args.skip_collect:
        print("\n  [--skip-collect] Using existing data")
    elif not check_data_exists():
        print_header("STEP 1/5: Collecting World Cup data")
        ok = run_script("collect_all_worldcups.py")
        steps.append(("Collect data", ok))
        all_ok &= ok
    else:
        print("\n  [OK] Data already exists at data/raw/worldcup_all.csv")

    # ── Step 2: Train model ─────────────────────────────
    if args.predict_only:
        steps.append(("Train model", True))
        print("\n  [--predict-only] Using existing model, skipping training")
    else:
        print_header(f"STEP 2/5: Training {args.model.upper()} model")
        ok = run_script("train_worldcup.py", ["--model", args.model])
        steps.append(("Train model", ok))
        all_ok &= ok

    # ── Step 3: Already done! train_worldcup.py trains AND predicts.
    # predictions saved to reports/predictions_worldcup/worldcup_predictions.csv
    steps.append(("Predict matches", True))
    print("  [OK] Predictions already generated by training step")

    # ── Step 4: Value bets ──────────────────────────────
    if args.skip_value_bets:
        print("\n  [--skip-value-bets] Skipping value bets")
        steps.append(("Value bets", True))
    else:
        print_header("STEP 4/5: Finding value bets with live odds")
        ok = run_script("today_value_bets_live.py", [
            "--calibrate", "platt",
            "--kelly", "0.25",
            "--days", "7",
        ])
        steps.append(("Value bets", ok))
        all_ok &= ok

    # ── Step 5: Launch dashboard ────────────────────────
    if args.skip_dashboard:
        print("\n  [--skip-dashboard] Skipping dashboard launch")
        steps.append(("Dashboard", True))
    else:
        print_header("STEP 5/5: Launching dashboard")
        print("  Starting Streamlit at http://localhost:8501")
        print()
        # Use run_dashboard.py which handles PYTHONPATH properly
        subprocess.Popen(
            [PYTHON, str(PROJECT_ROOT / "run_dashboard.py")],
            cwd=PROJECT_ROOT,
        )
        print("  Dashboard launching at: http://localhost:8501")
        steps.append(("Dashboard", True))

    # ── Summary ─────────────────────────────────────────
    elapsed = time.time() - t_start
    print()
    print("=" * 72)
    print(f"  RUN ALL - COMPLETE ({elapsed:.1f}s)")
    print("=" * 72)
    print(f"  {'Step':<25} {'Status':<15}")
    print(f"  {'-' * 40}")
    for name, ok in steps:
        status = "PASS" if ok else "FAIL"
        print(f"  {name:<25} {status:<15}")
    print(f"  {'-' * 40}")
    print(f"  Total time: {elapsed:.1f}s")
    print("=" * 72)
    print()
    print("  Dashboard:      http://localhost:8501")
    print("  Predictions:    reports/predictions_worldcup/worldcup_predictions.csv")
    print("  Value bets:     reports/value_bets/latest.csv")
    print("  Model:          models/worldcup_lightgbm.joblib")
    print("  Run guide:      guidesetupforpyay.text")
    print("=" * 72)

    return 0 if all_ok else 1


if __name__ == "__main__":
    sys.exit(main())
